package com.Iteration;

public class IterationExercise2 {

	public static void main(String[] args) {
		int numebrOfElements = 8;
		int nextElement = 1;
		System.out.println(nextElement);
		for(int i=1; i< numebrOfElements; i++)
		{
			nextElement = nextElement * 2;
			System.out.println(nextElement);
		}

	}

}
